import math

# Returns the ceiling of x, the smallest integer greater than or equal to x.
print(math.ceil(4.5))  # Output: 5

# Returns the floor of x, the largest integer less than or equal to x.
print(math.floor(4.8))  # Output: 4

# return factorial of given number
print(math.factorial(5))  # Output: 120

# Returns the greatest common divisor of a and b.
print(math.gcd(48, 18))  # Output: 6

# Returns the square root of x
print(math.sqrt(16))  # Output: 4.0

# Returns e raised to the power of x.
print(math.exp(2))  # Output: 7.38905609893065


# Returns the logarithm of x to the given base. If the base is not specified, returns the natural logarithm of x.
print(math.log(8, 2))  # Output: 3.0

# Returns the base-10 logarithm of x.
print(math.log10(100))  # Output: 2.0

#  Returns the base-2 logarithm of x.
print(math.log2(8))  # Output: 3.0


# Returns the sine of x radians.
print(math.sin(math.pi / 2))  # Output: 1.0

# Returns the cosine of x radians.
print(math.cos(0))  # Output: 1.0

# Returns the tangent of x radians.
print(math.tan(math.pi / 4))  # Output: 1.0


# Returns the arc sine of x, in radians.
print(math.asin(1))  # Output: 1.5707963267948966

# Returns the arc cosine of x, in radians.
print(math.acos(1))  # Output: 0.0


# Returns the arc tangent of x, in radians.
print(math.atan(1))  # Output: 0.7853981633974483


# Converts angle x from radians to degrees.
print(math.degrees(math.pi))  # Output: 180.0

# Converts angle x from degrees to radians.
print(math.radians(180))  # Output: 3.141592653589793


# Returns the Euclidean norm, sqrt(x*x + y*y).
print(math.hypot(3, 4))  # Output: 5.0

