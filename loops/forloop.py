
# sample basic loop
print("basic")
for i in range(10):
    print(i)
    
print("beginner")
for i in range(1,10):
    print(i)
    
print("medium")
for i in range(1,11):
    print(i)

    
print("advance")
for i in range(10):
    print(i+1)
    

print("Even in 1 to 20")
for i in range(20):
    if(i % 2 == 0 and i > 0):
        print(i)


print("Even in 1 to 20 without condition")
for i in range(2,20,2):
    print(i)
    
print("Even way")
for i in range(1,20,2):
    print(i+1)
