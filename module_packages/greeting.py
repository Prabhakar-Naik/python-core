
def birth_day_wish(name):
    return f"Wish you many more happy returns of the day {name}! Have fun your day."


def marriage_wish(groom,bride):
    return f"On your wedding day, I wish you hearty congratulations to both of you Mr.{groom}, Ms.{bride+" "+groom} and hope you have a wonderful journey ahead as you build a new life together."


def invitation_wish(date):
    return f"We have decided to throw a joyful party on {date}. You are cordially invited to share the joy with us. Your presence means a lot to us!"

